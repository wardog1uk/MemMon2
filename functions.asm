;-------------------------------------------------------------------------------
; convert lowest 4 bits of A to screen code
byteToScreen
        and #$0f
        clc
        adc #$30
        cmp #$3a
        bcc @done
        sbc #$39
@done   rts
;-------------------------------------------------------------------------------

;-------------------------------------------------------------------------------
; convert A from screen code to its value
; A must be '0'-'9' or 'A'-'F'
screenToByte
        clc
        adc #$09
        cmp #$10
        bcc @done
        sbc #$39
@done   rts
;-------------------------------------------------------------------------------
